# You can print several things - (*values) param
print(1, 2, 3, 4, 5)
print(True, False)
print('text1', 'text2', 'text3')

# Print with separator
print(9, 8, 7, 6, 5, sep=':::')

# Print with custom ending (by default it's new line)
print('Line 1 without break', end='.')
print('Line 2 on first line')

# We can print nothing, then we will just have new line
print()

# Getting help even here - equivalent to docs in PyCharm
help(print)
