# Absolute value
print(abs(-1.6))

# Convert int to float and vice versa
print(int(56.78899))
print(float(78))

# If string is number, it may be converted to number
print(int('6'))
print(float('-7.89'))

# Round with needed precision by maths rules
print(round(1.44550, 3))

# Guess the order of arithmetic (brackets -> ** -> *, /, % -> +, -)
print((4 - 9) * 5 ** 2 + 7)
print(9 / (2 + 4 * 3))
